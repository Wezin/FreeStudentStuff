import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const NOISY_TAG_SLUGS = new Set([
  "namecheap",
  "figma-education-plan",
  "craft-for-education",
  "notion-edu",
  "azure-for-students",
  "jotform-edu",
]);

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlArray(values) {
  if (!values.length) return "array[]::text[]";
  return `array[${values.map((v) => sqlString(v)).join(", ")}]`;
}

function fixListing(raw, copyBySlug) {
  const copy = copyBySlug[raw.slug];
  const source_url = copy?.source_url ?? raw.final_url ?? raw.source_url;
  const tags = copy?.tags ?? (NOISY_TAG_SLUGS.has(raw.slug) ? ["Deals", "Online"] : ["Deals", "Online"]);
  const description = copy?.description ?? raw.description;

  const { final_url: _finalUrl, ...rest } = raw;
  return {
    ...rest,
    description,
    source_url,
    tags,
    listing_type: "deal",
    cta_label: rest.cta_label?.trim() || "Get Deal",
  };
}

function toInsertRow(listing) {
  return `(
  ${sqlString(listing.id)}::uuid,
  ${sqlString(listing.title)},
  ${sqlString(listing.slug)},
  ${sqlString(listing.description)},
  ${sqlString(listing.listing_type)},
  ${listing.location ? sqlString(listing.location) : "null"},
  null,
  null,
  ${sqlArray(listing.tags)},
  ${sqlString(listing.thumbnail_url)},
  ${sqlString(listing.source_url)},
  ${sqlString(listing.cta_label)},
  ${listing.establishment_id ? sqlString(listing.establishment_id) : "null"},
  ${sqlString(listing.establishment_name)},
  ${sqlString(listing.status)},
  ${listing.is_featured},
  ${sqlString(listing.created_at)}::timestamptz,
  ${sqlString(listing.updated_at)}::timestamptz
)`;
}

function toUpdateStatement(listing) {
  const sets = [
    `description = ${sqlString(listing.description)}`,
    `tags = ${sqlArray(listing.tags)}`,
    `listing_type = 'deal'`,
    `source_url = ${sqlString(listing.source_url)}`,
    `cta_label = ${sqlString(listing.cta_label)}`,
    `updated_at = now()`,
  ];

  return `update listings set\n  ${sets.join(",\n  ")}\nwhere slug = ${sqlString(listing.slug)};`;
}

const inputPath = join(root, "supabase/data/freeforstudents-deals.raw.json");
const copyPath = join(root, "supabase/data/freeforstudents-deal-copy.json");
const outputJsonPath = join(root, "supabase/data/freeforstudents-deals.json");
const outputInsertPath = join(root, "supabase/insert_freeforstudents_deals.sql");
const outputUpdatePath = join(root, "supabase/update_freeforstudents_deals.sql");

const raw = JSON.parse(readFileSync(inputPath, "utf8"));
const copyBySlug = JSON.parse(readFileSync(copyPath, "utf8"));
const listings = raw.map((item) => fixListing(item, copyBySlug));

const missingCopy = listings.filter((l) => !copyBySlug[l.slug]);
if (missingCopy.length > 0) {
  console.warn("Missing polished copy for:", missingCopy.map((l) => l.slug).join(", "));
}

writeFileSync(outputJsonPath, `${JSON.stringify(listings, null, 2)}\n`);

const insertSql = `-- Free Plug — FreeForStudents deal listings (cleaned)
-- Generated by scripts/generate-freeforstudents-sql.mjs
-- Safe to re-run: upserts on slug

insert into listings (
  id, title, slug, description, listing_type, location, starts_at, ends_at,
  tags, thumbnail_url, source_url, cta_label, establishment_id, establishment_name,
  status, is_featured, created_at, updated_at
) values
${listings.map(toInsertRow).join(",\n")}
on conflict (slug) do update set
  title = excluded.title,
  description = excluded.description,
  listing_type = excluded.listing_type,
  location = excluded.location,
  starts_at = excluded.starts_at,
  ends_at = excluded.ends_at,
  tags = excluded.tags,
  thumbnail_url = excluded.thumbnail_url,
  source_url = excluded.source_url,
  cta_label = excluded.cta_label,
  establishment_id = excluded.establishment_id,
  establishment_name = excluded.establishment_name,
  status = excluded.status,
  is_featured = excluded.is_featured,
  updated_at = excluded.updated_at;
`;

const updateSql = `-- Free Plug — polish FreeForStudents deal listings already in DB
-- Generated by scripts/generate-freeforstudents-sql.mjs
-- Updates descriptions, tags (Deals + Online), and fixes a few source URLs

begin;

${listings.map(toUpdateStatement).join("\n\n")}

commit;
`;

writeFileSync(outputInsertPath, insertSql);
writeFileSync(outputUpdatePath, updateSql);

console.log(`Wrote ${listings.length} listings to:`);
console.log(`  ${outputJsonPath}`);
console.log(`  ${outputInsertPath}`);
console.log(`  ${outputUpdatePath}`);
